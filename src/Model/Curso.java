public class Curso {

    private Integer nroEstudiantes;
    private String codCurso;
    private String nroAula;
    private Integer id;
    private Integer id_malla;
    private Integer id_horario;

    /**
     * @return the nroEstudiantes
     */
    public Integer getNroEstudiantes() {
        return nroEstudiantes;
    }

    /**
     * @param nroEstudiantes the nroEstudiantes to set
     */
    public void setNroEstudiantes(Integer nroEstudiantes) {
        this.nroEstudiantes = nroEstudiantes;
    }

    /**
     * @return the codCurso
     */
    public String getCodCurso() {
        return codCurso;
    }

    /**
     * @param codCurso the codCurso to set
     */
    public void setCodCurso(String codCurso) {
        this.codCurso = codCurso;
    }

    /**
     * @return the nroAula
     */
    public String getNroAula() {
        return nroAula;
    }

    /**
     * @param nroAula the nroAula to set
     */
    public void setNroAula(String nroAula) {
        this.nroAula = nroAula;
    }

    /**
     * @return the id
     */
    public Integer getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * @return the id_malla
     */
    public Integer getId_malla() {
        return id_malla;
    }

    /**
     * @param id_malla the id_malla to set
     */
    public void setId_malla(Integer id_malla) {
        this.id_malla = id_malla;
    }

    /**
     * @return the id_horario
     */
    public Integer getId_horario() {
        return id_horario;
    }

    /**
     * @param id_horario the id_horario to set
     */
    public void setId_horario(Integer id_horario) {
        this.id_horario = id_horario;
    }
    

}
